class MyClass5 { 
    public static void main(String args[]) 
    { 
        char a = 'Q'; 
        int i = 89; 
        boolean b = true; 
        short s = 56; 
        double d = 4.35; 
        float f = 4.73f; 
		
        System.out.println("char: " + a); 
        System.out.println("integer: " + i); 
        System.out.println("boolean: " + b); 
        System.out.println("short: " + s); 
        System.out.println("float: " + f); 
        System.out.println("double: " + d); 
    } 
} 